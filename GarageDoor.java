public class GarageDoor {
    public void up() { System.out.println("Porta da garagem subiu."); }
    public void down() { System.out.println("Porta da garagem desceu."); }
    public void stop() { System.out.println("Porta da garagem parada."); }
    public void lightOn() { System.out.println("Luz da garagem ligada."); }
    public void lightOff() { System.out.println("Luz da garagem desligada."); }
}
